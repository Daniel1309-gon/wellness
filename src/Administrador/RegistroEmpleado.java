    private void btnAbrirRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirRegistroActionPerformed
        Login interLogin = new Login();

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/prueba1", user, password);

            PreparedStatement pst = con.prepareStatement("INSERT INTO infoempleados VALUES(?,?,?,?)");

            if(!txtID.getText().isEmpty() && !txtCargo.getText().isEmpty() && !txtNombre.getText().isEmpty() && !txtArea.getText().isEmpty()){
            pst.setInt(1, Integer.parseInt(txtID.getText().trim()));
            pst.setString(2, txtNombre.getText().trim());
            pst.setString(3, txtCargo.getText().trim());
            pst.setString(4, txtArea.getText().trim());
            
            pst.executeUpdate();
            
            MainAdmin mainAdmin = new MainAdmin();
            mainAdmin.setVisible(true);
            this.setVisible(false);    
            } else{
                JOptionPane.showMessageDialog(null, "Datos incompletos");
            }
        } catch (SQLException e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_btnAbrirRegistroActionPerformed

