    public boolean ValidarEmail(String em) {
        Pattern pat = Pattern.compile("^[\\w-]+(\\.[\\w-]+)*@[A-Za-z0-9]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher match = pat.matcher(em);
        if (match.find()) {
            return true;
        } else {
            return false;
        }
    }

